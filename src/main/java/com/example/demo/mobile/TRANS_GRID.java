package com.example.demo.mobile;

public class TRANS_GRID {
	private String gridCode,gridDesc;

	public TRANS_GRID(String gridCode, String gridDesc) {
		super();
		this.gridCode = gridCode;
		this.gridDesc = gridDesc;
	}

	public String getGridCode() {
		return gridCode;
	}

	public void setGridCode(String gridCode) {
		this.gridCode = gridCode;
	}

	public String getGridDesc() {
		return gridDesc;
	}

	public void setGridDesc(String gridDesc) {
		this.gridDesc = gridDesc;
	}
	
	

}
