package com.example.demo.mobile;

public class GenerationDash {
	private String hdr,linex,netGen;

	public GenerationDash(String hdr, String linex, String netGen) {
		super();
		this.hdr = hdr;
		this.linex = linex;
		this.netGen = netGen;
	}

	public String getHdr() {
		return hdr;
	}

	public void setHdr(String hdr) {
		this.hdr = hdr;
	}

	public String getLinex() {
		return linex;
	}

	public void setLinex(String linex) {
		this.linex = linex;
	}

	public String getNetGen() {
		return netGen;
	}

	public void setNetGen(String netGen) {
		this.netGen = netGen;
	}
	
	

}
